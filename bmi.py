#รับน้ำหนัก (kg) แปลงเป็นตัวเลขทศนิยม
weight = float(input())

#รับส่วนสูง (เมตร)
height = float(input())

#ใช้สูตร BMI = น้ำหนัก ÷ (ส่วนสูง²)
bmi = weight / (height ** 2)

#แสดงผลทศนิยม 2 ตำแหน่ง
print(f"{bmi:.2f}")

#< 18.5 = น้ำหนักน้อย 18.5–24.9 = ปกติ 25–29.9 = น้ำหนักเกิน ≥ 30 = อ้วน
if bmi < 18.5:
    print("น้ำหนักน้อย")
elif bmi < 25:
    print("ปกติ")
elif bmi < 30:
    print("น้ำหนักเกิน")
else:
    print("อ้วน")
